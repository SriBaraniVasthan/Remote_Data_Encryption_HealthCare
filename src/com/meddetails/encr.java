package com.meddetails;

public class encr {
	
	private String encry;
	private String decry;

	public encr(String encry, String decry) {
		super();
		this.encry = encry;
		this.decry = decry;
	}

	public String getEncry() {
		return encry;
	}

	public void setEncry(String encry) {
		this.encry = encry;
	}

	public String getDecry() {
		return decry;
	}

	public void setDecry(String decry) {
		this.decry = decry;
	}

	
	

}
